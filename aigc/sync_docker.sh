#!/bin/bash

# -----------------------------
# 配置路径
# -----------------------------
SRC="/mnt/nfs/samba/Document/aigc/"
DST="/home/dooven/aigc/"
# -----------------------------
# 设置权限
# -----------------------------
echo "设置权限为 777"
chmod -R 777 "$DST"

# -----------------------------
# 同步文件
# -----------------------------
echo "同步文件: $SRC -> $DST"
rsync -av --update "$SRC" "$DST"
# -----------------------------
# 重启 Docker 容器
# -----------------------------
echo "重启 Docker 容器: aigc"
docker restart aigc

echo "完成！"
